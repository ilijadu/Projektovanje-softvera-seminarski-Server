    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        try {
            if (proveraPolja()) {
                String url = txtUrl.getText();
                String username = txtUsername.getText();
                String password = txtPassword.getText();

                properties.put(ServerKonstante.DB_CONFIG_URL, url);
                properties.store(new FileOutputStream(ServerKonstante.FILE_PATH), null);
                properties.put(ServerKonstante.DB_CONFIG_USERNAME, username);
                properties.store(new FileOutputStream(ServerKonstante.FILE_PATH), null);
                properties.put(ServerKonstante.DB_CONFIG_PASSWORD, password);
                properties.store(new FileOutputStream(ServerKonstante.FILE_PATH), null);
                this.dispose();
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(konfiguracijeForma.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(konfiguracijeForma.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(konfiguracijeForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

