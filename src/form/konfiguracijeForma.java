    private boolean proveraPolja() {
        if (txtUrl.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Polje \"URL\" ne sme biti prazno!");
            return false;
        } else if (txtUsername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Polje \"USERNAME\" ne sme biti prazno!");
            return false;
        } else if (txtPassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Polje \"PASSWORD\" ne sme biti prazno!");
            return false;
        }
        return true;
    }

    private void popuniFormu() {
        try {
            properties = new Properties();
            properties.load(new FileInputStream(ServerKonstante.FILE_PATH));
            txtUrl.setText(properties.getProperty(ServerKonstante.DB_CONFIG_URL));
            txtUsername.setText(properties.getProperty(ServerKonstante.DB_CONFIG_USERNAME));
            txtPassword.setText(properties.getProperty(ServerKonstante.DB_CONFIG_PASSWORD));
        } catch (Exception ex) {
            Logger.getLogger(konfiguracijeForma.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

